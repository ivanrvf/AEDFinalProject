    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        int row = enterpriseJTable.getSelectedRow();
        if(row >= 0){
            StateNetwork stateNetwork = (StateNetwork) enterpriseJTable.getValueAt(row, 2);
            Enterprise enterprise = (Enterprise) enterpriseJTable.getValueAt(row, 0);
            stateNetwork.getEnterpriseDirectory().deleteEnterprise(enterprise);
            JOptionPane.showMessageDialog(null, "Successfully removed "+ enterprise.getName() +"!");
            populateTable();
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select an enterprise");
        }
    }//GEN-LAST:event_deleteJButtonActionPerformed


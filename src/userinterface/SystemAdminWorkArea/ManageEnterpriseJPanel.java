    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);
        for (CountryNetwork country : system.getCountryList()) {
            for (StateNetwork network : country.getStateNetwork()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    Object[] row = new Object[4];
                    row[0] = enterprise;
                    row[1] = country;
                    row[2] = network;
                    row[3] = enterprise.getEnterpriseType().getValue();
                    model.addRow(row);
                }
            }
        }
    }
    
    private void populateStateComboBox(CountryNetwork country) {
        stateJComboBox.removeAllItems();
        if(country!= null){
            for(StateNetwork network : country.getStateNetwork()) {
                stateJComboBox.addItem(network);
            }
        }
    }

    private void populateCountryComboBox() {
        countryJComboBox.removeAllItems();
        enterpriseTypeJComboBox.removeAllItems();
        for (CountryNetwork country : system.getCountryList()) {
            countryJComboBox.addItem(country);
        }
        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            enterpriseTypeJComboBox.addItem(type);
        }
    }

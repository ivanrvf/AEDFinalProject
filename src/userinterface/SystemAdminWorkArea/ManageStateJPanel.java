    private void deleteNetworkJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteNetworkJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = networkJTable.getSelectedRow();
        
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please select a row");
            return;
            
        }
        
        StateNetwork network = (StateNetwork) networkJTable.getValueAt(selectedRow, 0);
        CountryNetwork country = (CountryNetwork)countryJComboBox.getSelectedItem();
        if(country!= null){
        country.removeState(network);
        JOptionPane.showMessageDialog(null,"State network removed successfully");
        populateStateTable();
        }
        
    }//GEN-LAST:event_deleteNetworkJButtonActionPerformed


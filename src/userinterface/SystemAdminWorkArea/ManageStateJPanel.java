    private void addNetworkJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNetworkJButtonActionPerformed

        String name = nameJTextField.getText();

        if (!name.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(null, "State name should contain only letters", "Info", JOptionPane.WARNING_MESSAGE);
            return;
        }
        CountryNetwork country = (CountryNetwork)countryJComboBox.getSelectedItem();
        
        if(country!=null){
            StateNetwork network = country.addStateNetwork();
            network.setName(name);
            JOptionPane.showMessageDialog(null,"State network added successfully");
            populateStateTable();
        }
        
        else{
        JOptionPane.showMessageDialog(null,"Please select a row");
        }

        
    }//GEN-LAST:event_addNetworkJButtonActionPerformed


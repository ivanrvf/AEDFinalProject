    private void populateCountryComboBox() {
        countryJComboBox.removeAllItems();
        
        if(system.getCountryList()!=null){

        for (CountryNetwork country : system.getCountryList()) {
            countryJComboBox.addItem(country);
        }
        }

    }

    private void populateStateTable() {
        DefaultTableModel model = (DefaultTableModel) networkJTable.getModel();

        model.setRowCount(0);
        CountryNetwork country = (CountryNetwork)countryJComboBox.getSelectedItem();
        if(country!=null){
        for (StateNetwork network : country.getStateNetwork()) {
            Object[] row = new Object[1];
            row[0] = network;
            model.addRow(row);
        }
        }
    }


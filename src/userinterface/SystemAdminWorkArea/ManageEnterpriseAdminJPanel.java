    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterpriseJTable.getModel();
        model.setRowCount(0);
        CountryNetwork country = (CountryNetwork)countryJComboBox1.getSelectedItem();
        if(country!=null){
            for (StateNetwork network : country.getStateNetwork()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                        Object[] row = new Object[3];
                        row[0] = enterprise;
                        row[1] = network;
                        row[2] = userAccount;
                        model.addRow(row);
                    }
                }
            }
        }
    }

    private void populateNetworkComboBox(){
        networkJComboBox.removeAllItems();
        CountryNetwork country = (CountryNetwork)countryJComboBox.getSelectedItem();
        if(country !=null){
            for(StateNetwork network : country.getStateNetwork()){
                networkJComboBox.addItem(network);
            }
        }
    }
        
    
    private void populateCountryComboBox() {
        countryJComboBox.removeAllItems();
        if(system.getCountryList()!=null){
            for (CountryNetwork country : system.getCountryList()) {
                countryJComboBox.addItem(country);
                countryJComboBox1.addItem(country);
            }
        }
    }
    
    private void populateEnterpriseComboBox(StateNetwork network){
        enterpriseJComboBox.removeAllItems();
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            enterpriseJComboBox.addItem(enterprise);
        }
    }
    

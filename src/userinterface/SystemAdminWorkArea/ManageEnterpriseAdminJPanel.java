    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        int row = enterpriseJTable.getSelectedRow();
        if(row >= 0){
            Enterprise enterprise = (Enterprise) enterpriseJTable.getValueAt(row, 0);
            UserAccount userAccount = (UserAccount) enterpriseJTable.getValueAt(row, 2);
            enterprise.getUserAccountDirectory().deleteUserAccount(userAccount);
            JOptionPane.showMessageDialog(null, "Successfully deleted "+ userAccount.getUsername() +" !");
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select a user account!");
        }
    }//GEN-LAST:event_deleteJButtonActionPerformed


    private void acceptDeliveryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptDeliveryJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            return;
        }

        OrderRequest request = (OrderRequest)workRequestJTable.getValueAt(selectedRow, 0);
        String status = request.getStatus();
        if (status.equals("Processed by Logistics, Out for Delivery")){
        request.setStatus("Delivered");
        request.getLastOrderRequest().setStatus("Delivered");
        request.setResolveDate();
        request.getModeOfTransport().setStatus("Available");
        //organization.getInventoryCatalog().addNewItem(request.getVaccine(),request.getQuantity());
        JOptionPane.showMessageDialog(null,"Your order has been delivered !");
        }
        
        
        else{
            JOptionPane.showMessageDialog(null,"This order is not out for delivery yet !");
        }
        
        
        
       
        
        
    }//GEN-LAST:event_acceptDeliveryJButtonActionPerformed


    public void populateOrderTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (OrderRequest request : userAccount.getOrderQueue().getOrderRequestList()){
            Object[] row = new Object[7];
            row[0] = request;
            row[1] = "1";//request.getSupplier().getSupplierName()== null ? null : request.getSupplier();
            row[2] = request.getQuantity();
            row[3] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[4] = request.getStatus();
            row[5] = request.getRequestDate();
            row[6] = request.getResolveDate() == null ? null : request.getResolveDate();
                       
            model.addRow(row);
        }
    
    }


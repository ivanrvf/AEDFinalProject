    public void loadVaccineComboBox(Disease disease){
    vaccineListJComboBox.removeAllItems();
        for(Vaccine vaccine: vaccineCatalog.getVaccineList()){
    if(vaccine.getDiseaseProtectedAgainst().equals(disease)){
    vaccineListJComboBox.addItem(vaccine);
    }
    
    }
    vaccineListJComboBox.addItem("Blood pack Request");
    }


    private void placeOrderJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_placeOrderJButtonActionPerformed
        // TODO add your handling code here:
        OrderRequest request = null;
        if(coordinates==null){
        JOptionPane.showMessageDialog(null,"Please set the coordinates");
        return;
        }
        for(CDCEnterprise enterprise: network.getEnterpriseDirectory().getCDCEnterprise()){
            request = enterprise.getOrderQueue().addOrderRequest();
        }
        if(vaccineListJComboBox.getSelectedItem()!="Blood pack Request"){
            request.setVaccine((Vaccine)vaccineListJComboBox.getSelectedItem());}
        
        else if(vaccineListJComboBox.getSelectedItem().toString().equals("Blood pack Request")){
            
            String bloodGroup = ((Patient)patientListJComboBox.getSelectedItem()).getBloodGroup();
            Disease disease = ((Patient)patientListJComboBox.getSelectedItem()).getDisease();
            Blood blood = new Blood(bloodGroup,disease);     
            request.setVaccine(blood);
                }
        else{return;}
        request.setDestinationCoordinates(coordinates);
        request.setQuantity((Integer)quantityJSpinner.getValue());
        request.setPatient((Patient)patientListJComboBox.getSelectedItem());
        request.setSender(userAccount);
        request.setStatus("Order Placed");
//        
//        OrderRequest nextRequest = new OrderRequest();
//        
//        nextRequest.setSender(userAccount);
//        nextRequest.setStatus("Order Placed");
//        request.assignOrderRequest(nextRequest);
        OrderRequest lastRequest = request.getLastOrderRequest();
            
        lastRequest.createAndGetNewOrderRequest().setSender(userAccount);
        lastRequest.createAndGetNewOrderRequest().setStatus("Order Placed");
        

        /*
        lastRequest.createAndGetNewOrderRequest().setSender(userAccount);
        lastRequest.createAndGetNewOrderRequest().setStatus("Order Placed");
        */
        JOptionPane.showMessageDialog(null,"Order placed successfully !");
        //Organization org = null;
        userAccount.getOrderQueue().getOrderRequestList().add(request);
    }//GEN-LAST:event_placeOrderJButtonActionPerformed


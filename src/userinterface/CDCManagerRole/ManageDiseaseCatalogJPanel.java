    private void removeDiseaseJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeDiseaseJButtonActionPerformed
        // TODO add your handling code here:
         int selectedRow = diseaseCatalogJTable.getSelectedRow();
        
        if (selectedRow < 0){
            return;
        }
        
        Disease disease = (Disease) diseaseCatalogJTable.getValueAt(selectedRow, 1);
        diseaseCatalog.removeDisease(disease);
        populateTable();
        
    }//GEN-LAST:event_removeDiseaseJButtonActionPerformed

